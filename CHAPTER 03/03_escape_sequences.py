# escape sequences: \n, \t, \', \", \\

#  \n - next line
story1 = "once upon a time in a land far, far away there lived a king and a queen.\nThe king was very kind and the queen was very beautiful."
print(story1)
#  \t - tab space
story2 = "once upon a time in a land far, far away there lived a king and a queen.\n\tThe king was very kind and the queen was very beautiful."
print(story2)

# \' - single quote
story3 = 'once upon a time in a land far, far away there lived a king and a queen.\n\tThe king was very kind and the queen was very beautiful.\n\t\tThe king said, \'I love you\' to the queen.'
print(story3)

# \" - double quote
story4 = "once upon a time in a land far, far away there lived a king and a queen.\n\tThe king was very kind and the queen was very beautiful.\n\t\tThe king said, \"I love you\" to the queen."
print(story4)

# \\ - backslash
story5 = "once upon a time in a land far, far away there lived a king and a queen.\n\tThe king was very kind and the queen was very beautiful.\n\t\tThe king said, \"I love you\" to the queen.\n\t\t\tThe king\'s crown is made of gold \\ silver."
print(story5)